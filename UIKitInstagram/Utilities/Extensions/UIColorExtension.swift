import UIKit

extension UIColor {
    class var lightBlackDarkWhite: UIColor {
        return UIColor(named: "Black-White")!
    }
    
    class var defaultIconColor: UIColor {
        return UIColor(named: "DefaultIconColor")!
    }
}
